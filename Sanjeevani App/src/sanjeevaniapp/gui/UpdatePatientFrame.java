    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        boolean isInputValid=validateInputs();
      if(!isInputValid){
          JOptionPane.showMessageDialog(null,"Please input all fields","Error",JOptionPane.ERROR_MESSAGE);
          return;
      }
     
      try{
           PatientPojo p=new  PatientPojo();
           p.setPatientId(jcPatId.getSelectedItem().toString());
           p.setAddress(txtAreaAddress.getText().trim());
           p.setAge(Integer.parseInt(txtAge.getText().trim()));
          p.setGender(jcGender.getSelectedItem().toString());
           p.setCity(txtCity.getText());
           p.setDoctorId(jcDocId.getSelectedItem().toString());
           p.setFirstName(txtfname.getText().trim());
           p.setLastName(txtlname.getText().trim());
           p.setMno(txtPhoneno.getText().trim());
           p.setOpd(txtOPD.getText());
           p.setMarriedStatus(jcMarial.getSelectedItem().toString());
           p.setAptStatus("REQUEST");
           AppointmentPojo app=new AppointmentPojo();
          app.setMobileNo(txtPhoneno.getText().trim());
          app.setPatientId(jcPatId.getSelectedItem().toString());
          app.setOpd(txtOPD.getText());
          app.setStatus("REQUEST");
          app.setPatientName(txtfname.getText()+" "+txtlname.getText());
          app.setDoctorName(DoctorsDao.getDoctorNameById(jcDocId.getSelectedItem().toString()));
         // app.setAppointmentDate(patient.getDate().toString());
         // System.out.println(p);
         // System.out.println(app);
          boolean result=PatientDao.updatePatient(p);
          boolean result2=AppointmentDao.updateAppointment(app);
         // System.out.println(result +" - "+result2);
          if(result && result2){
          
              JOptionPane.showMessageDialog(null,"Record updated successfully!","Success",JOptionPane.INFORMATION_MESSAGE);
              //clearAll();
              return;
              
          }
          JOptionPane.showMessageDialog(null,"Record not updated!","Failure!",JOptionPane.ERROR_MESSAGE);
      }
      catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed


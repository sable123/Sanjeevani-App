    private void jcPatIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcPatIdActionPerformed
        if(jcPatId.getItemCount()==0){
                    btnUpdate.setEnabled(false);
                    JOptionPane.showMessageDialog(null, "NO patients record found");
                
            }
       
        try{
            String patId=jcPatId.getSelectedItem().toString();
            PatientPojo rec=PatientDao.getPatienttDetails(patId);
            txtAge.setText(String.valueOf(rec.getAge()));
            txtAreaAddress.setText(rec.getAddress());
            txtCity.setText(rec.getCity());
            txtDate.setText(String.valueOf(rec.getDate()));
            jcDocId.setSelectedItem(rec.getDoctorId());
            jcMarial.setSelectedItem(rec.getMarriedStatus());
            txtOPD.setText(rec.getOpd());
            txtPhoneno.setText(rec.getMno());
            txtfname.setText(rec.getFirstName());
            txtlname.setText(rec.getLastName());
            jcGender.setSelectedItem(rec.getGender());
            
        }
        catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    }//GEN-LAST:event_jcPatIdActionPerformed

